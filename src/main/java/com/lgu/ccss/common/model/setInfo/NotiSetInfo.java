public class NotiSetInfo {
	
	private String notiSchedDepart="On";
	
	private String notiDestDepart="On";
	
	private String notice="On";
	
	private String cacaoLocationShare="On";
	
	public String getNotiSchedDepart() {
		return notiSchedDepart;
	}
	public void setNotiSchedDepart(String notiSchedDepart) {
		this.notiSchedDepart = notiSchedDepart;
	}
	public String getNotiDestDepart() {
		return notiDestDepart;
	}
	public void setNotiDestDepart(String notiDestDepart) {
		this.notiDestDepart = notiDestDepart;
	}
	public String getNotice() {
		return notice;
	}
	public void setNotice(String notice) {
		this.notice = notice;
	}
	public String getCacaoLocationShare() {
		return cacaoLocationShare;
	}
	public void setCacaoLocationShare(String cacaoLocationShare) {
		this.cacaoLocationShare = cacaoLocationShare;
	}
}
