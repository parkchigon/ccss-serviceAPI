package com.lgu.common.map.model.route;

public class RGDataJSON {	
	
	private String directionName;
	private String lengthToNextLink;
	private String CrossName;
	private String RoadName;
	private String RGType;
	private String RGImage;
	private String RGSound;
	private String lonx;
	private String laty;
	
	public String getDirectionName() {
		return directionName;
	}
	public void setDirectionName(String directionName) {
		this.directionName = directionName;
	}
	public String getLengthToNextLink() {
		return lengthToNextLink;
	}
	public void setLengthToNextLink(String lengthToNextLink) {
		this.lengthToNextLink = lengthToNextLink;
	}
	public String getCrossName() {
		return CrossName;
	}
	public void setCrossName(String crossName) {
		CrossName = crossName;
	}
	public String getRoadName() {
		return RoadName;
	}
	public void setRoadName(String roadName) {
		RoadName = roadName;
	}
	public String getRGType() {
		return RGType;
	}
	public void setRGType(String rGType) {
		RGType = rGType;
	}
	public String getRGImage() {
		return RGImage;
	}
	public void setRGImage(String rGImage) {
		RGImage = rGImage;
	}
	public String getRGSound() {
		return RGSound;
	}
	public void setRGSound(String rGSound) {
		RGSound = rGSound;
	}
	public String getLonx() {
		return lonx;
	}
	public void setLonx(String lonx) {
		this.lonx = lonx;
	}
	public String getLaty() {
		return laty;
	}
	public void setLaty(String laty) {
		this.laty = laty;
	}

	
	
	
}
